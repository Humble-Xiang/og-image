
import { readFileSync } from 'fs';
import { marked } from 'marked';
import { sanitizeHtml } from './sanitizer';
import { ParsedRequest } from './types';
const twemoji = require('twemoji');
const twOptions = { folder: 'svg', ext: '.svg' };
const emojify = (text: string) => twemoji.parse(text, twOptions);

const recursive1 = readFileSync(`${__dirname}/../_fonts/Recursive01.woff`).toString('base64');
const recursive2 = readFileSync(`${__dirname}/../_fonts/Recursive02.woff`).toString('base64');
const recursive3 = readFileSync(`${__dirname}/../_fonts/Recursive03.woff`).toString('base64');
const recursive4 = readFileSync(`${__dirname}/../_fonts/Recursive04.woff`).toString('base64');
const ceyy = readFileSync(`${__dirname}/../_fonts/仓耳渔阳体W02.woff2`).toString('base64');

